

public class Logical {
    public static void main(String[] args) {
        // both logic should be true
        System.out.println((3>2) && (4>2));

        // if any of || operator is true then it will be true
        // if both OR operator is false then it will be false either it will be true
        System.out.println((3>5) || (5 < 30)); // one statement is true so it will answer true
        
    }
}
